public class DailyForecast {
    String date, temperatureMinF, temperatureMaxF, dayIcon, nightIcon, mobileLink, link, minUnit, maxUnit, dayIconID, nightIconID;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTemperatureMinF() {
        return temperatureMinF;
    }

    public void setTemperatureMinF(String temperatureMinF) {
        this.temperatureMinF = temperatureMinF;
    }

    public String getTemperatureMaxF() {
        return temperatureMaxF;
    }

    public void setTemperatureMaxF(String temperatureMaxF) {
        this.temperatureMaxF = temperatureMaxF;
    }

    public String getDayIcon() {
        return dayIcon;
    }

    public void setDayIcon(String dayIcon) {
        this.dayIcon = dayIcon;
    }

    public String getNightIcon() {
        return nightIcon;
    }

    public void setNightIcon(String nightIcon) {
        this.nightIcon = nightIcon;
    }

    public String getMobileLink() {
        return mobileLink;
    }

    public void setMobileLink(String mobileLink) {
        this.mobileLink = mobileLink;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public String getMinUnit() {
        return minUnit;
    }

    public void setMinUnit(String minUnit) {
        this.minUnit = minUnit;
    }

    public String getMaxUnit() {
        return maxUnit;
    }

    public void setMaxUnit(String maxUnit) {
        this.maxUnit = maxUnit;
    }

    public String getDayIconID() {
        return dayIconID;
    }

    public void setDayIconID(String dayIconID) {
        this.dayIconID = dayIconID;
    }

    public String getNightIconID() {
        return nightIconID;
    }

    public void setNightIconID(String nightIconID) {
        this.nightIconID = nightIconID;
    }
}
